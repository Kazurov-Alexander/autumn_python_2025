#todo: Дана сторона квадрата a. Найти его площадь S = a²
# Примечание: сторону квадрата получаем через функцию input().

a = float(input("Введите длину стороны квадрата: ")) # Получаем значение стороны квадрата от пользователя

S = a ** 2                                           # Вычисляем площадь квадрата по формуле S = a²

print("Площадь квадрата: ", S)

