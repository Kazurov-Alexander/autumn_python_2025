#Формат вывода:
#Количество букв a - 13
#Количество букв o - 12
#Количество букв e - 11
#.....................

from collections import Counter

# Список гласных (кириллица)
vowels = 'АЕЁИОУЫЭЮЯаеёиоуыэюя'

# Считаем гласные
vowel_counter = Counter()

with open('dump.txt', encoding='utf-8') as f:
    for line in f:
        for char in line:
            if char in vowels:
                vowel_counter[char.lower()] += 1  # считаем в нижнем регистре

# Вывод статистики
for vowel, count in sorted(vowel_counter.items()):
    print(f'Количество букв {vowel} - {count}')

# Вывод после запуска:
