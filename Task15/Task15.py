#todo: Дан целочисленный массив размера N из 10 элементов.
#Преобразовать массив, увеличить каждый его элемент на единицу.

mass = [5, 8, 12, 0, -3, 7, 9, 1, 4, 6] # Исходный массив из 10 целых чисел

# Создаём новый массив, где каждый элемент увеличен на 1
mass_plus_one = [x + 1 for x in mass]  # list comprehension — компактный способ перебора и преобразования

# Выводим исходный массив
print("Исходный массив:", mass)

# Выводим преобразованный массив
print("Преобразованный массив:", mass_plus_one)