# todo: Единицы массы пронумерованы следующим образом: 1 — килограмм, 2 — миллиграмм, 3 — грамм,
#  4 — тонна, 5 — центнер. Дан номер единицы массы и масса тела M в этих единицах (вещественное число).
#  Вывести массу данного тела в килограммах

def convert_to_kg(unit_code, mass):                 # Функция принимает код единицы массы и значение массы
    conversion_factors = {                          # Словарь коэффициентов перевода в килограммы
        1: 1,                                       # килограмм → килограмм
        2: 1e-6,                                    # миллиграмм → килограмм
        3: 1e-3,                                    # грамм → килограмм
        4: 1000,                                    # тонна → килограмм
        5: 100                                      # центнер → килограмм
    }

    factor = conversion_factors.get(unit_code)      # Получаем коэффициент по номеру единицы массы
    
    if factor is None:                              # Если номер единицы некорректен — выводим сообщение
        return "Неверный код!"

    return mass * factor                            # Возвращаем массу в килограммах

unit = int(input("""1-киллограмм; \n2-миллиграмм; 
                 \n3-грамм; \n4-тонна; \n5-центнер.\n
                 Указажите номер единицы измерения (1–5): """))      # Вводим номер единицы измерения
value = float(input("Указать значение массы: "))                     # Вводим массу
result = convert_to_kg(unit, value)                                  # Переводим в килограммы
print("Масса в киллограммах: ", result, "кг. ")                      # Выводим результат

