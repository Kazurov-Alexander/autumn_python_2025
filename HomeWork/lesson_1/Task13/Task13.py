# В восточном календаре принят 60-летний цикл, состоящий из 12- летних подциклов,
# обозначаемых названиями цвета: зеленый, красный, желтый, белый и черный.
# В каждом подцикле годы носят названия животных: крысы, коровы, тигра, зайца, дракона,
# змеи, лошади, овцы, обезьяны, курицы, собаки и свиньи. По номеру года вывести его название,
# если 1984 год был началом цикла — годом зеленой крысы.

def get_eastern_year_name(year):
    animals = [                                                                 # Список животных в порядке 12-летнего цикла
        "крысы", "коровы", "тигра", "зайца", "дракона", "змеи",
        "лошади", "овцы", "обезьяны", "курицы", "собаки", "свиньи"
    ]

    animal_genders = {                                                          # Род животных: 'm' — мужской, 'f' — женский, чтобы не было чёрного крысы и зелёной тигра
        "крысы": "f", "коровы": "f", "тигра": "m", "зайца": "m", "дракона": "m",
        "змеи": "f", "лошади": "f", "овцы": "f", "обезьяны": "f",
        "курицы": "f", "собаки": "f", "свиньи": "f"
    }

    colors = [                                                                  # Цвета в родительном падеже (кого/чего)
        ("Зелёного", "Зелёной"),
        ("Красного", "Красной"),
        ("Жёлтого", "Жёлтой"),
        ("Белого", "Белой"),
        ("Чёрного", "Чёрной")
    ]

    offset = year - 1984                                                        # Вычисляем смещение от базового года 1984
    color_index = offset % 10 // 2                                              # индекс цвета, каждые два года один цвет
    animal_index = offset % 12                                                  # Индекс животного: 12-летний цикл

    animal = animals[animal_index]                                              # Получаем название животного по [индексу]
    gender = animal_genders[animal]                                             # Получаем род животного ('m' или 'f')
    color = colors[color_index][0 if gender == "m" else 1]                      # Выбираем форму цвета в зависимости от рода животного

    return f"{color} {animal}"                                                  # Возвращаем строку с названием года: цвет + животное

year_input = int(input("Введите номер года: "))                                 # Запрашиваем ввод года у пользователя
print(f"Название года: {get_eastern_year_name(year_input)}")                    # Выводим название года по восточному календарю